        // Animate the counting
        $({
          countNum: $this.text()
        }).animate({
            countNum: countTo
          },
          {
            duration: 3000,
            easing: 'swing',
            step: function() {
              $this.text(Math.floor(this.countNum));  // Update number during animation
            },
            complete: function() {
              $this.text(this.countNum);  // Set the final value after animation is complete
            }
          });
      });
      a = 1;  // Prevent the counter from running again
    }
  });
