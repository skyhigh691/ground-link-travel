        // Get the form by ID
const form = document.getElementById('MbusCont_frm');

form.addEventListener('submit', function (event) {
    event.preventDefault(); // Prevent form submission

    // Clear previous error messages
    document.querySelectorAll('.error-message').forEach(msg => msg.remove());

    let isValid = true;

    // Helper function to add error messages
    function addErrorMessage(input, message) {
        const error = document.createElement('span');
        error.className = 'error-message text-danger';
        error.textContent = message;
        input.parentElement.appendChild(error);
    }

    // Validate first name
    const firstName = form.querySelector('input[placeholder="Your first name"]');
    if (!firstName.value.trim()) {
        addErrorMessage(firstName, 'First name is required.');
        isValid = false;
    }

    // Validate last name
    const lastName = form.querySelector('input[placeholder="Your last name"]');
    if (!lastName.value.trim()) {
        addErrorMessage(lastName, 'Last name is required.');
        isValid = false;
    }

    // Validate email address
    const email = form.querySelector('input[type="email"]');
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!email.value.trim()) {
        addErrorMessage(email, 'Email address is required.');
        isValid = false;
    } else if (!emailRegex.test(email.value)) {
        addErrorMessage(email, 'Please enter a valid email address.');
        isValid = false;
    }

    // Validate phone number
    const phone = form.querySelector('input[placeholder="Your phone number"]');
    if (!phone.value.trim()) {
        addErrorMessage(phone, 'Phone number is required.');
        isValid = false;
    }

    // Validate pick-up location
    const pickup = form.querySelector('select[aria-label="Large select example"]');
    if (pickup.selectedIndex === 0) {
        addErrorMessage(pickup, 'Please select a pick-up location.');
        isValid = false;
    }

    // Validate drop-off location
    const dropOff = form.querySelectorAll('select[aria-label="Large select example"]')[1];
    if (dropOff.selectedIndex === 0) {
        addErrorMessage(dropOff, 'Please select a drop-off location.');
        isValid = false;
    }

    // Validate message
    const message = form.querySelector('textarea[placeholder="write your message"]');
    if (!message.value.trim()) {
        addErrorMessage(message, 'Message is required.');
        isValid = false;
    }

    // If valid, submit the form or handle success
    if (isValid) {
        alert('Form submitted successfully!');
        // You can submit the form via AJAX or proceed with a default submission
        // form.submit();
    }
});
        
        
