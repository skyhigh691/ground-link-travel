document.getElementById("formvalidation").addEventListener("submit", function (event) {
  event.preventDefault(); // Prevent default form submission

  // Clear previous error messages
  const errorElements = document.querySelectorAll(".error-message");
  errorElements.forEach(el => el.remove());

  let isValid = true;

  // Helper function to display error messages
  function showError(input, message) {
    const errorElement = document.createElement("div");
    errorElement.className = "error-message";
    errorElement.style.color = "red";
    errorElement.style.fontSize = "0.9em";
    errorElement.textContent = message;
    input.parentElement.appendChild(errorElement);
    isValid = false;
  }

  // Validate Leaving From Section
  const leavingFrom = document.querySelector("input[name='leavingFrom']");
  if (!leavingFrom.value.trim()) {
    showError(leavingFrom, "Please enter a starting point/location.");
  }

  const leavingDate = document.querySelector("input[name='leavingDate']");
  if (!leavingDate.value) {
    showError(leavingDate, "Please select a valid date.");
  }

  const leavingTime = document.querySelector("input[name='leavingTime']");
  if (!leavingTime.value) {
    showError(leavingTime, "Please select a valid time.");
  }

  // Validate Going To Section
  const goingFrom = document.querySelector("input[name='goingfrom']");
  if (!goingFrom.value.trim()) {
    showError(goingFrom, "Please enter a destination point/location.");
  }

  const goingDate = document.querySelector("input[name='goingdate']");
  if (!goingDate.value) {
    showError(goingDate, "Please select a valid date.");
  }

  const goingTime = document.querySelector("input[name='goingtime']");
  if (!goingTime.value) {
    showError(goingTime, "Please select a valid time.");
  }

  // Final validation check
  if (isValid) {
    alert("Form submitted successfully!");
    this.submit(); // Only submit the form if all validations pass
  }
});

